    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

   int num_cliente = Integer.parseInt(txtClienteId.getText());
   String nombre_perro = txtNombre.getText();
   String raza = txtRaza.getText();
   String color = txtColor.getText();
   String alergico = (String) cmbAlergia.getSelectedItem();  
   String atención_especial = (String) cmbAtencion.getSelectedItem();
   String nombre_duenio = txtNomDue.getText();
   int celular_duenio = Integer.parseInt(txtCel.getText());
   String observaciones = txtObser.getText();
  
   
   control.altaDatosMascotas(num_cliente, nombre_perro, raza, color, alergico, atención_especial, nombre_duenio, celular_duenio, observaciones);



        
    }//GEN-LAST:event_btnAgregarActionPerformed

